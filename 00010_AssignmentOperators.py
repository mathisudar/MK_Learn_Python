# Assignment Operators


"""
=   Assignment
+=	Addition
-=	Subtraction
*=	Multiplication
/=	Division
%=	Modulus
**=	Exponentiation
//=	Floor division
"""


a = 125
print(a)
a += 5  # a=a+5
print(a)
a -= 10  # a=a-10
print(a)
a *= 10  # a=a*10
print(a)
a /= 10
print(a)
a %= 10
print(a)
a **=10
print(a)
a //= 10 
print(a)



